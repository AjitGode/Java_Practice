package Operators;

public class relationOp {
    public static void main(String[] args) {
        int num1= 25;
        int num2= 50;

        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1==num2);
        System.out.println(num1>=num2);
        System.out.println(num1!=num2);
        System.out.println(num1<=num2);
    }
}
