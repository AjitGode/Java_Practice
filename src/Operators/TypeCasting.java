package Operators;

public class TypeCasting {
     public static void main(String[] args) {
         int x=20;
         long p = x;
         System.out.println(p);

         double y =(double) x;
         System.out.println(y);

         byte z= (byte)x;
         System.out.println(z);

         char a='a';
         int b= (int) a;
         System.out.println(b);
     }
}
