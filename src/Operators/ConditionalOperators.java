package Operators;

public class ConditionalOperators {
    public static void main(String[] args) {
        System.out.println("Conditional Operators");

        System.out.println(50<70 && 60<80);
        System.out.println(50<70 && 90<80);
        System.out.println(100<70 && 90<80);
        System.out.println(50<70 || 60<80);
        System.out.println(50<70 || 90<80);
        System.out.println(100<70 || 90<80);
    }
}
